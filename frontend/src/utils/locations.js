/**
 * Get locations grouped by area for optgroup display
 */
export const getLocationsByArea = () => {
  const grouped = {};
  
  JAKARTA_LOCATIONS.forEach(location => {
    if (!grouped[location.area]) {
      grouped[location.area] = [];
    }
    grouped[location.area].push(location);
  });
  
  return grouped;
};

/**
 * Search locations by query string
 */
export const searchLocations = (query) => {
  if (!query) return JAKARTA_LOCATIONS;
  
  const lowercaseQuery = query.toLowerCase();
  return JAKARTA_LOCATIONS.filter(location => 
    location.label.toLowerCase().includes(lowercaseQuery) ||
    location.area.toLowerCase().includes(lowercaseQuery)
  );
};

/**
 * Get location by value
 */
export const getLocationByValue = (value) => {
  return JAKARTA_LOCATIONS.find(location => location.value === value);
};
